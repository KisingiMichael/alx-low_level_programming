#include "3-calc.h"

/**
 * op_add - Returns the sum of the numbers
 * @a: The first number
 * @b: The second number
 *
 * Return: The sum of numbers
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - Returns the difference of numbers
 * @a: The first number
 * @b: The second number
 *
 * Return: The difference of numbers
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - Returns the product of the numbers
 * @a: The first number
 * @b: The second number
 *
 * Return: The product of numbers
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - Returns the division of the numbers
 * @a: The first number
 * @b: The second number
 *
 * Return: The quotient of numbers
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - Returns the remainder of the division of numbers
 * @a: The first number
 * @b: The second number
 *
 * Return: modulus of a/b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
